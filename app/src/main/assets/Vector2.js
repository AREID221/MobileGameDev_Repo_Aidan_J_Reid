// Vector2 forms the basis of a coordinate system for game objects.
// Two values are given to the constructor when Vector2 is declared elsewhere.
// Ex: x = 10, y = 20.

class Vector2
{
    constructor(x, y)
    {
        this.x = x; // Make a passed in value equal x. 
        this.y = y; // Make a passed in value equal y.
    }
}